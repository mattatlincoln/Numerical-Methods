## Assessments

This module will have two types of assessment

- formative weekly short questions
- Time Controlled Assessment assignments

### Formative Weekly tests 
will be marked on a very simple scheme to allow rapid feedback:
    
- 100 for a correct answer
- 0 for an incorrect answer without supporting material

Feedback will be provided by partially worked solutions that will be presented in the tutorial session. There will be time in the workshop sessions to get extra feedack from  the lecturers and demonstrators on your work.

!!! warn
These assessments will not contribute to your mark but are essential to learning the module material. You are strongly advised to attempt the questions!

### Assignments
assignments will consist of mini projects to be tackled over a period of a few days e.g. construct a predictive model from this data using methods you have seen in the module. They will be marked on an extended rubric detailing requirements for each degree classification:

- Clear explanation of the problem addressed in mathematical terms
- Explanation of the choice of method applied
- Details of the numerical method implementation in your chosen language
- Quality of the code - including comments, and testing.
- Presentation of results and analysis 

A full rubric will be provided and examples given leading up to the assignments.

## Deadlines

Weekly short questions will be opened to you in the Monday session on the material covered in the online lecture. They should be submitted online by 3 the following Monday. This will allow feedback to be given in the following sessions.

There will be two assignments. They will be towards the end semester 1 and 2 you can see the times on the assessment timetable on Bb for all your modules.

## Logbooks

Logbooks will not be directly assessed in this module.

!!! note 
we really do recommend that you keep a logbook for your own benefit. It is good professional practice and ... they will help you a lot in completing the assignments in a reasonable amount of time.

Also, when submitting work you should attach evidence of your working, which would be very similar to relevant pages from your logbook.

## Module Contents 

- Curve fitting. 
- Numerical solution of non-linear equations.
- Numerical solution of ordinary differential equations. 
- Solving systems of equations.
- Numerical solution of partial differential equations.
- Elements of stochastic methods.

